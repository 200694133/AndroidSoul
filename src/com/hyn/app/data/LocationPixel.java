public class LocationPixel {
    double mLat;
    double mLng;
    double mRatio;

    public double getLat() {
        return mLat;
    }

    public void setLat(double mLat) {
        this.mLat = mLat;
    }

    public double getLng() {
        return mLng;
    }

    public void setLng(double mLng) {
        this.mLng = mLng;
    }

    public double getRatio() {
        return mRatio;
    }

    public void setRatio(double mRatio) {
        this.mRatio = mRatio;
    }
}
